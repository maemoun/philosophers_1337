#include "philo.h"

int	ft_atoi(char *s)
{
	int	i;
	int	total;

	i = 0;
	total = 0;
	if (s[i] == '-')
		return (-1);
	if (s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		total = (total * 10) + (s[i] - 48);
		i++;
	}
	if (!(s[i] >= '0' && s[i] <= '9') && s[i] != '\0')
		return (-1);
	return (total);
}
